#Скрипт, який малює драбину
##Кількість сходинок в драбині передається скрипту в якості аргумента
###Гарантуєтсья, що на вхід подаються тільки цілі числа > 0
####Сходинки відображатимуться за допомогою символу коментування "#" та пробілів
import sys

steps = int(sys.argv[1])

for i in range(steps+1):
    if(i == 0):
        continue
    print((" "*(steps-i))+"#"*i)
