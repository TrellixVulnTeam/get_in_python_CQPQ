#Програма, яка запускається з командної стрічки
##Програма приймає в якості аргументу стрічку, яка складається з цифр
###Гарантується, що інших символів в переданому аргументі немає і на вхід завжди подається непуста стрічка
####Програма вираховує суму цифр, переданих програмі в якості аргумента та вивести в стандарний вивід

import sys

digit_string = sys.argv[1]
result = 0

for i in digit_string:
    result += int(i)

print(result)
