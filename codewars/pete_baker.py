#Функція приймає два аргументи та повертає максимальну кількість тортів, яку піт зможе спекти
##Перший аргумент це рецепт потрібен для випікання 1 торту
###Другий аргумент це кількість інгрідієнтів на даний момент
def cakes(d1, d2):
    if len(d1.keys() - d2.keys())!= 0:
        return 0
    return sorted([x for x in {k: int(d2[k]/d1[k]) for k in d1.keys() & d2.keys()}.values()])[0]
